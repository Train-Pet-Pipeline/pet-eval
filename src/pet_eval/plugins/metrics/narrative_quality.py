"""Narrative quality metric using BERTScore for pet-eval.

Computes the mean BERTScore F1 between model-generated narratives and
teacher-generated reference narratives using a Chinese BERT model.
"""

from __future__ import annotations

import logging

import bert_score
from pet_infra.registry import METRICS

from pet_eval.plugins.metrics.types import MetricResult

logger = logging.getLogger(__name__)

_CHINESE_BERT_MODEL = "bert-base-chinese"


def compute_narrative_quality(
    model_narratives: list[str],
    teacher_narratives: list[str],
    *,
    threshold: float | None = None,
) -> MetricResult:
    """Compute mean BERTScore F1 between model and teacher narratives.

    Uses the ``bert-base-chinese`` model to evaluate semantic similarity
    between generated and reference Chinese pet narratives.

    Args:
        model_narratives: List of narratives generated by the model under evaluation.
        teacher_narratives: List of reference narratives from the teacher model.
        threshold: Optional gate threshold for the ``"gte"`` operator.
            Pass ``None`` for an informational-only metric.

    Returns:
        A single :class:`MetricResult` with ``name="narrative_bertscore"`` and
        ``operator="gte"``. Value is the mean F1 across all samples.
        ``details`` contains ``mean_precision``, ``mean_recall``, and
        ``per_sample_f1`` list.
    """
    if not model_narratives or not teacher_narratives:
        logger.warning(
            "compute_narrative_quality: empty inputs; returning 0.0",
        )
        return MetricResult.create(
            "narrative_bertscore",
            value=0.0,
            threshold=threshold,
            operator="gte",
            details={"mean_precision": 0.0, "mean_recall": 0.0, "per_sample_f1": []},
        )

    precision, recall, f1 = bert_score.score(
        model_narratives,
        teacher_narratives,
        model_type=_CHINESE_BERT_MODEL,
        verbose=False,
    )

    mean_f1 = float(f1.mean())
    mean_precision = float(precision.mean())
    mean_recall = float(recall.mean())
    per_sample_f1 = f1.tolist()

    logger.info(
        "compute_narrative_quality",
        extra={
            "mean_f1": mean_f1,
            "mean_precision": mean_precision,
            "mean_recall": mean_recall,
        },
    )

    return MetricResult.create(
        "narrative_bertscore",
        value=mean_f1,
        threshold=threshold,
        operator="gte",
        details={
            "mean_precision": mean_precision,
            "mean_recall": mean_recall,
            "per_sample_f1": per_sample_f1,
        },
    )


# ---- Registry adapter (P2-B) ----


@METRICS.register_module(name="narrative_quality")
class NarrativeQualityMetric:
    """Registry adapter wrapping compute_narrative_quality."""

    def __init__(self, **kwargs) -> None:
        """Store kwargs to forward to compute_narrative_quality."""
        self._kwargs = kwargs

    def __call__(self, *args, **call_kwargs) -> MetricResult:
        """Delegate to compute_narrative_quality with merged kwargs."""
        return compute_narrative_quality(*args, **{**self._kwargs, **call_kwargs})
